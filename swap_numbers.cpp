#include<iostream>
using namespace std;
int main()
{
    /*First method for swapping two numbers*/
    int a,b,temp;
    cin>>a>>b;
    temp=a;
    a=b;
    b=temp;
    cout<<"After swapping: "<<a<<" "<<b<<endl;
    /*Second method for swapping two numbers: */
    int x,y,z;
    cin>>x>>y;
    z=x+y;
    x=z-x;
    y=z-y;
    cout<<"After swapping : "<<x<<" "<<y;

}
